;
; Describe how you could define tables using a binary tree
;
;
; Answer:
;  One way to do this would be to take the keys
;  and combine them into a single key. Then use
;  the single key to decide where to place the key
;  and value in the binary tree.  Each node of the
;  binary tree would be a pair of combined keys and
;  value.
